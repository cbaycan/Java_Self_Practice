        String studentName;
        String age;
        String gender;
        String schoolName;
        String studentId;
        String gradeLevel;
        String gpa;
        studentName = "Josh Steven";
        age = "22";
        gender = "Male";
        schoolName = "MIT";
        studentId = "A125";
