package day02_variables_practice_tasks;

public class Birthday {
    public static void main(String[] args) {

        String name;
        String birthDay;
        String birthMonth;
        String birthYear;

        name = "John";
        birthDay = "25";
        birthMonth = "April";
        birthYear = "1995";

        System.out.println(name + " was born on " + birthMonth + birthDay + ", " + birthYear);

    }
}
