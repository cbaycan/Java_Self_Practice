package day05_practice_tasks;

public class EnglishAlphabets {
    public static void main(String[] args) {

        for (char i = 'a'; i <= 'z'; i++) {

            for (char j = 'A'; j <= 'E'; j++) {
                System.out.println(j);
            }

            System.out.print(i + " ");
        }
        System.out.println();

    }
}
