package day05_loops;

public class SquareOfNumbers {
    public static void main(String[] args) {


        System.out.println("Num\t|\tSquare of Num");
        System.out.println("---------------------------");
        for (int i = 1; i < 11; i++) {
            System.out.println(i + "\t|\t" + (i*i));

        }






    }
}
